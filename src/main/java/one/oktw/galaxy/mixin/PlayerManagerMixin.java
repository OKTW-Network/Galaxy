package one.oktw.galaxy.mixin;

import net.minecraft.network.chat.Component;
import net.minecraft.server.PlayerManager;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

@Mixin(PlayerManager.class)
public class PlayerManagerMixin {
    @Inject(
        method = "Lnet/minecraft/server/PlayerManager;broadcastChatMessage(Lnet/minecraft/network/chat/Component;Z)V",
        at = @At("HEAD")
    )
    public void onBroadcastChatMessage(Component component_1, boolean isSystemMessage, CallbackInfo ci) {
        if (isSystemMessage) return;
        ci.cancel();
    }
}
